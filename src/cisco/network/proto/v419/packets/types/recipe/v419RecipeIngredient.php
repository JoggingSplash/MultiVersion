final class v419RecipeIngredient
{

	public function __construct(
		protected int $id,
		protected int $meta,
		protected int $count
	)
	{

	}

	public function getId() : int
	{
		return $this->id;
	}

	public function getCount() : int
	{
		return $this->count;
	}

